package mae.general.jreports;

public class UbicacionGD {
	private int id;
	private String nombre;
	
	public UbicacionGD(int id, String nombre) {
		this.id = id;
		this.nombre = nombre;
	}

	public int getId() {
		return id;
	}

	public String getNombre() {
		return nombre;
	}
	
}
